public class Operators {
    public static void main(String[] args) {

        //Unary Operators Example : ++ and --

        int x=10;
        System.out.println(x++);//10(11)
        System.out.println(++x);//12
        System.out.println(x--);//12(11)
        System.out.println(--x);//10


    }
}
